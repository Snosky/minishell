#include "libft.h"

size_t	ft_intlen(int n)
{
	int				i;

	i = 0;
	if (n == 0)
		return (1);
	else if (n < 0)
		i += 1;
	while (n /= 10)
		i++;
	i += 1;
	return (i);
}
